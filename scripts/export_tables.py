def _find_latest_tables(results_dir: Path) -> tuple[list[dict], list[dict], list[dict]]:
    """
    Locate the most recent table JSON files in results_dir.
    Returns (table2_rows, table3_rows, table4_rows).
    """
    def _latest(pattern: str) -> Path | None:
        candidates = sorted(results_dir.glob(pattern), reverse=True)
        return candidates[0] if candidates else None

    t2_path = _latest("table2_main_results_*.json")
    t3_path = _latest("table3_error_analysis_*.json")
    t4_path = _latest("table4_ablation_*.json")

    # Fall back to combined file
    combined = _latest("all_tables_*.json")
    if combined and (not t2_path or not t3_path):
        data = json.loads(combined.read_text())
        return data.get("table2", []), data.get("table3", []), data.get("table4", [])

    t2 = json.loads(t2_path.read_text()) if t2_path else []
    t3 = json.loads(t3_path.read_text()) if t3_path else []
    t4 = json.loads(t4_path.read_text()) if t4_path else []
    return t2, t3, t4


