def _table2_latex(rows: list[dict]) -> str:
    lines = [
        r"% Table 2 — Main Evaluation Results",
        r"% Generated by NL-SIEM export_tables.py",
        r"\begin{table}[t]",
        r"\centering",
        r"\setlength{\tabcolsep}{4pt}",
        r"\caption{",
        r"    Evaluation results for NL-SIEM across five SIEM platforms.",
        r"    \emph{Valid\%}: syntactic validity; \emph{Sem.}: semantic score;",
        r"    \emph{BLEU}: BLEU-4; \emph{F1}: field-match F1;",
        r"    \emph{Exact\%}: exact execution match.",
        r"}",
        r"\label{tab:main_results}",
        r"\begin{tabular}{l r r r r r r r}",
        r"\toprule",
        r"\textbf{Platform} & \textbf{Valid\%} & \textbf{Sem.} & \textbf{BLEU} & "
        r"\textbf{ROUGE-L} & \textbf{F1} & \textbf{Exact\%} & \textbf{Recall\%} \\",
        r"\midrule",
    ]
    for r in rows:
        is_avg = r.get("platform") == "MACRO-AVG"
        if is_avg:
            lines.append(r"\midrule")
        bold = r"\textbf{" if is_avg else ""
        endb = r"}"          if is_avg else ""
        line = (
            f"{bold}{r.get('platform','?')}{endb} & "
            f"{bold}{r.get('validity_%','0.0')}{endb} & "
            f"{bold}{r.get('semantic_score','0.0000')}{endb} & "
            f"{bold}{r.get('bleu','0.0000')}{endb} & "
            f"{bold}{r.get('rouge_l','0.0000')}{endb} & "
            f"{bold}{r.get('field_f1','0.0000')}{endb} & "
            f"{bold}{r.get('exact_match_%','0.0')}{endb} & "
            f"{bold}{r.get('recall_match_%','0.0')}{endb} \\\\"
        )
        lines.append(line)
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    return "\n".join(lines) + "\n"


def _table3_latex(rows: list[dict]) -> str:
    lines = [
        r"% Table 3 — Error Analysis",
        r"% Generated by NL-SIEM export_tables.py",
        r"\begin{table}[t]",
        r"\centering",
        r"\caption{",
        r"    Error taxonomy for NL-SIEM translation failures.",
        r"    \emph{Err\%}: overall error rate; columns show per-category counts.",
        r"}",
        r"\label{tab:error_analysis}",
        r"\begin{tabular}{l r r r r r r r l}",
        r"\toprule",
        r"\textbf{Platform} & \textbf{Err\%} & \textbf{Syn} & \textbf{Fld} & "
        r"\textbf{Op} & \textbf{Log} & \textbf{Tmp} & \textbf{Plt} & \textbf{Top Error} \\",
        r"\midrule",
    ]
    for r in rows:
        line = (
            f"{r.get('platform','?')} & "
            f"{r.get('error_rate_%','0.0')} & "
            f"{r.get('syntax_errors',0)} & "
            f"{r.get('field_errors',0)} & "
            f"{r.get('operator_errors',0)} & "
            f"{r.get('logic_errors',0)} & "
            f"{r.get('temporal_errors',0)} & "
            f"{r.get('platform_errors',0)} & "
            f"\\texttt{{{r.get('top_error','none')}}} \\\\"
        )
        lines.append(line)
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    return "\n".join(lines) + "\n"


def _table4_latex(rows: list[dict]) -> str:
    if not rows:
        return "% Table 4 — Ablation (no data)\n"
    lines = [
        r"% Table 4 — Ablation Study",
        r"% Generated by NL-SIEM export_tables.py",
        r"\begin{table}[t]",
        r"\centering",
        r"\caption{",
        r"    Ablation study: contribution of IR schema and RAG retrieval.",
        r"    A = Baseline LLM; B = IR + LLM; C = Full System (IR + RAG + Refinement).",
        r"    $\Delta$ = improvement over Condition A.",
        r"}",
        r"\label{tab:ablation}",
        r"\begin{tabular}{l l r r r r r}",
        r"\toprule",
        r"\textbf{Cond.} & \textbf{Platform} & \textbf{Valid\%} & \textbf{Sem.} & "
        r"\textbf{BLEU} & \textbf{F1} & \textbf{$\Delta$} \\",
        r"\midrule",
    ]
    current_cond = None
    for r in rows:
        cond = r.get("condition", "?")
        if cond != current_cond:
            if current_cond is not None:
                lines.append(r"\midrule")
            current_cond = cond
        label = r.get("condition_label", cond)
        delta = r.get("delta_vs_A", "0.0000")
        sign  = "+" if str(delta).startswith("0.") or str(delta) == "0.0000" else ""
        line  = (
            f"{cond} ({label[:10]}) & "
            f"{r.get('platform','?')} & "
            f"{r.get('validity_%','0.0')} & "
            f"{r.get('semantic_score','0.0000')} & "
            f"{r.get('bleu','0.0000')} & "
            f"{r.get('field_f1','0.0000')} & "
            f"{sign}{delta} \\\\"
        )
        lines.append(line)
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    return "\n".join(lines) + "\n"


# ─────────────────────────────────────────────────────────────────────────────
# Markdown exporters
# ─────────────────────────────────────────────────────────────────────────────

def _to_markdown(rows: list[dict], title: str) -> str:
    if not rows:
        return f"### {title}\n\n_(no data)_\n"
    cols  = list(rows[0].keys())
    lines = [f"### {title}", ""]
    lines.append("| " + " | ".join(cols) + " |")
    lines.append("| " + " | ".join(["---"] * len(cols)) + " |")
    for r in rows:
        lines.append("| " + " | ".join(str(r.get(c, "")) for c in cols) + " |")
    lines.append("")
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# CSV exporters
# ─────────────────────────────────────────────────────────────────────────────

def _to_csv(rows: list[dict]) -> str:
    if not rows:
        return ""
    buf  = StringIO()
    cols = list(rows[0].keys())
    w    = csv.DictWriter(buf, fieldnames=cols)
    w.writeheader()
    w.writerows(rows)
    return buf.getvalue()


# ─────────────────────────────────────────────────────────────────────────────
# Stdout printer
# ─────────────────────────────────────────────────────────────────────────────

def _print_table(title: str, rows: list[dict]) -> None:
    if not rows:
        print(f"\n{title}\n  (no data)\n")
        return
    cols   = list(rows[0].keys())
    widths = {c: max(len(c), max(len(str(r.get(c,""))) for r in rows)) for c in cols}
    sep    = "+" + "+".join("-" * (w + 2) for w in widths.values()) + "+"
    hdr    = "|" + "|".join(f" {c:<{widths[c]}} " for c in cols) + "|"
    print(f"\n{title}")
    print(sep); print(hdr); print(sep)
    for r in rows:
        print("|" + "|".join(f" {str(r.get(c,'')):<{widths[c]}} " for c in cols) + "|")
    print(sep)


# ─────────────────────────────────────────────────────────────────────────────
# Main export function
# ─────────────────────────────────────────────────────────────────────────────

def export(
    t2:         list[dict],
    t3:         list[dict],
    t4:         list[dict],
    output_dir: Path,
    formats:    list[str],
    print_only: bool,
) -> dict[str, Path]:
    """
    Export tables to all requested formats.

    Args:
        t2, t3, t4:  Table rows as plain dicts (from JSON files).
        output_dir:  Directory to write output files.
        formats:     Subset of ["latex", "markdown", "csv", "json"].
        print_only:  If True, print to stdout only.

    Returns:
        Dict of format_name → Path.
    """
    ts      = time.strftime("%Y%m%d_%H%M%S")
    paths:  dict[str, Path] = {}

    if print_only or "latex" in formats:
        t2_tex = _table2_latex(t2)
        t3_tex = _table3_latex(t3)
        t4_tex = _table4_latex(t4)
        if print_only:
            print("\n" + "=" * 64 + "  LaTeX — Table 2\n")
            print(t2_tex)
            print("=" * 64 + "  LaTeX — Table 3\n")
            print(t3_tex)
            if t4:
                print("=" * 64 + "  LaTeX — Table 4\n")
                print(t4_tex)
        if not print_only and "latex" in formats:
            output_dir.mkdir(parents=True, exist_ok=True)
            for name, content in [
                (f"table2_main_{ts}.tex",   t2_tex),
                (f"table3_errors_{ts}.tex", t3_tex),
                (f"table4_ablation_{ts}.tex", t4_tex),
            ]:
                p = output_dir / name
                p.write_text(content)
                paths[name] = p

    if print_only or "markdown" in formats:
        md = "\n".join([
            "# NL-SIEM Evaluation Results\n",
            f"_Generated: {time.strftime('%Y-%m-%d %H:%M:%S')}_\n",
            _to_markdown(t2, "Table 2 — Main Results"),
            _to_markdown(t3, "Table 3 — Error Analysis"),
            _to_markdown(t4, "Table 4 — Ablation Study") if t4 else "",
        ])
        if print_only:
            print("\n" + "=" * 64 + "  Markdown\n")
            print(md[:2000], "…" if len(md) > 2000 else "")
        if not print_only and "markdown" in formats:
            output_dir.mkdir(parents=True, exist_ok=True)
            p = output_dir / f"results_{ts}.md"
            p.write_text(md)
            paths["markdown"] = p

    if not print_only and "csv" in formats:
        output_dir.mkdir(parents=True, exist_ok=True)
        for name, rows in [("table2", t2), ("table3", t3), ("table4", t4)]:
            if rows:
                p = output_dir / f"{name}_{ts}.csv"
                p.write_text(_to_csv(rows))
                paths[f"{name}_csv"] = p

    if not print_only and "json" in formats:
        output_dir.mkdir(parents=True, exist_ok=True)
        p = output_dir / f"all_tables_{ts}.json"
        p.write_text(json.dumps({"table2": t2, "table3": t3, "table4": t4,
                                  "generated_at": ts}, indent=2))
        paths["json"] = p

    if print_only:
        print("\n──  Stdout export complete  ──\n")
        _print_table("TABLE 2 — Main Results",  t2)
        _print_table("TABLE 3 — Error Analysis", t3)
        if t4:
            _print_table("TABLE 4 — Ablation",  t4)

    return paths


