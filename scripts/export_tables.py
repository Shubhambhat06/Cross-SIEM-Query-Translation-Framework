def main() -> int:
    parser = argparse.ArgumentParser(
        description="Export evaluation tables to LaTeX / Markdown / CSV / JSON.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--results",    type=Path, default=DEFAULT_RESULTS,
                        help="Directory containing run_evaluation.py output files")
    parser.add_argument("--output",     type=Path, default=DEFAULT_OUTPUT,
                        help="Directory to write exported files")
    parser.add_argument("--formats",    nargs="+",
                        choices=["latex", "markdown", "csv", "json"],
                        default=["latex", "markdown", "csv", "json"],
                        help="Output formats to generate")
    parser.add_argument("--print-only", action="store_true",
                        help="Print to stdout; do not write files")
    parser.add_argument("--ablation",   action="store_true",
                        help="Include Table 4 ablation (requires ablation data)")
    parser.add_argument("--quiet",      action="store_true")
    args = parser.parse_args()

    if not args.quiet:
        print("\n" + "=" * 64)
        print("  NL-SIEM  |  Table Exporter")
        print("=" * 64)
        print(f"  results : {args.results}")
        print(f"  output  : {args.output}")
        print(f"  formats : {args.formats}\n")

    # Load table data
    if not args.results.exists():
        print(f"[ERROR]  Results directory not found: {args.results}\n"
              f"         Run: python scripts/run_evaluation.py", file=sys.stderr)
        return 2

    try:
        t2, t3, t4 = _find_latest_tables(args.results)
    except Exception as exc:
        print(f"[ERROR]  Could not load tables: {exc}", file=sys.stderr)
        log.exception("Table loading failed")
        return 2

    if not t2 and not t3:
        print("[ERROR]  No table data found in results directory.", file=sys.stderr)
        return 2

    if not args.ablation:
        t4 = []

    if not args.quiet:
        print(f"  Loaded: Table2={len(t2)} rows, Table3={len(t3)} rows, Table4={len(t4)} rows")

    # Export
    try:
        paths = export(
            t2         = t2,
            t3         = t3,
            t4         = t4,
            output_dir = args.output,
            formats    = args.formats,
            print_only = args.print_only,
        )
    except Exception as exc:
        log.exception("Export failed")
        print(f"[ERROR]  {exc}", file=sys.stderr)
        return 2

    if not args.print_only and not args.quiet:
        print(f"\n[OK]  {len(paths)} files written to: {args.output}")
        for name, p in paths.items():
            print(f"       {name:<30} → {p.name}")
        print()

    return 0


if __name__ == "__main__":
    sys.exit(main())