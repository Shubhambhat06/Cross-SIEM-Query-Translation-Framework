#!/usr/bin/env python3
"""
scripts/export_tables.py
=========================
Export evaluation metrics into paper-ready formats:
  - LaTeX booktabs tables (Table 2, Table 3, Table 4)
  - Markdown tables (README / supplementary material)
  - CSV (for plotting / spreadsheet analysis)
  - Consolidated JSON (all tables in one file)

Reads from results/ produced by run_evaluation.py.

Usage
-----
    # Export from default results directory
    python scripts/export_tables.py

    # Custom results directory
    python scripts/export_tables.py --results results/eval_run_01/

    # Export only specific formats
    python scripts/export_tables.py --formats latex markdown

    # Override output directory
    python scripts/export_tables.py --output paper/tables/

    # Include ablation table
    python scripts/export_tables.py --ablation

    # Print all tables to stdout (no file writes)
    python scripts/export_tables.py --print-only

Exit codes: 0 success | 2 fatal
"""

